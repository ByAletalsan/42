int	ft_atoi(const char *str)
{
	int	sign;
	int	result;

	sign = 1;
	while (*str == ' ' || *str == '\t' || *str == '\n'
		|| *str == '\v' || *str == '\f' || *str == '\r')
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			sign *= -1;
		str++;
	}
	result = 0;
	while (*str >= '0' && *str <= '9')
	{
		result = (*str - 48) + (result * 10);
		str++;
	}
	return (result * sign);
}
