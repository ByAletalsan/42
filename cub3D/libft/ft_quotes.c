#include "libft.h"

int	quotes(char c, int *q)
{
	if (c == '\'' || c == '"')
	{
		if (q[0] == 0)
		{
			q[0] = (int)c;
			q[1] = 1;
			return (1);
		}
		else
		{
			if (q[0] == (int)c)
			{
				q[0] = 0;
				q[1] = 0;
				return (1);
			}
		}
	}
	return (0);
}
