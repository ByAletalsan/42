#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*p;

	i = 0;
	p = b;
	while (*p != '\0' && i < len)
	{
		*p = c;
		p += 1;
		i++;
	}
	return (b);
}
