#include<unistd.h>

void	ft_putstr(char *str)
{
	int				i;
	unsigned char	c;

	i = 0;
	c = str[i];
	while (c != '\0')
	{
		write(1, &c, 1);
		i++;
		c = str[i];
	}
	return ;
}
