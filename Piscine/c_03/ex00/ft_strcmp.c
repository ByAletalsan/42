int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	diff;

	diff = 0;
	i = 0;
	while (s1[i] > 0)
	{
		if (s1[i] != s2[i])
		{
			break ;
		}
		i++;
	}
	diff = s1[i] - s2[i];
	if (diff != 0)
		return (s1[i] - s2[i]);
	else
		return (0);
}
