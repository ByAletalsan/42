char	*ft_strcpy(char *dest, char *src)
{
	char	c;
	int		i;

	i = 0;
	c = src[i];
	while (c > 0)
	{
		dest[i] = c;
		c = src[++i];
	}
	dest[i] = '\0';
	return (dest);
}
